# evaluation.py
# This file defines the API route for evaluating a user's interview answer.
# The frontend sends the role, question, and the user's answer.
# We pass it to the AI evaluator and return the score and feedback.
# First step: Importing the Libraries

from fastapi import APIRouter, HTTPException
from models.schemas import AnswerSubmitRequest, EvaluationResponse
from services.evaluator import evaluate_answer

# Create a router for evaluation-related endpoints
# Registered in main.py under the /evaluation prefix
router = APIRouter()


@router.post("/submit", response_model=EvaluationResponse)
def submit_answer(request: AnswerSubmitRequest):
    """
    Receives the user's answer to an interview question and evaluates it.
    Returns a score out of 10, feedback, improvement suggestion,
    and a sample better answer generated by the AI.
    """

    # Basic validation - don't send empty answers to the AI
    # Strip whitespace to catch answers that are just spaces
    if not request.answer.strip():
        raise HTTPException(
            status_code=400,
            detail="Answer cannot be empty. Please type something before submitting."
        )

    # Call the evaluator service which sends the answer to Gemini
    # and returns a structured dictionary with score and feedback
    result = evaluate_answer(
        role=request.role,
        question=request.question,
        answer=request.answer
    )

    # Return the result as a structured response
    # Pydantic automatically validates the shape matches EvaluationResponse
    return EvaluationResponse(
        score=result["score"],
        feedback=result["feedback"],
        suggestion=result["suggestion"],
        sample_answer=result.get("sample_answer", "")
    )